// 网址(url,支持端口和"?+参数"和"#+参数)
export const url = /^(((ht|f)tps?):\/\/)?[\w-]+(\.[\w-]+)+([\w.,@?^=%&:/~+#-]*[\w@?^=%&/~+#-])?$/
// 必须带端口号的网址(或ip)
export const url_port = /^((ht|f)tps?:\/\/)?[\w-]+(\.[\w-]+)+:\d{1,5}\/?$/

// html注释
export const html_comment = /^<!--[\s\S]*?-->$/

// md5格式(32位)
export const md5 = /^([a-f\d]{32}|[A-F\d]{32})$/

// 版本号(version)格式必须为X.Y.Z
export const version = /^\d+(?:\.\d+){2}$/

// 视频(video)链接地址（视频格式可按需增删）
